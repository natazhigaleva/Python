# Дана произвольная строка. Cоздать строку, в которой содержатся только цифры из исходной строки

# Пpимер:
# input:
# АА”АА324А К*К№5 10 79

# output:
# 32451079

s = ("k@sb6&57hli?sd8!75%124")
s1 = filter(lambda s: s.isdigit(), s)
print(*s1, sep='')

# 657875124


