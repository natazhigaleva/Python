# Вывести на экран фигуру из звездочек
# *******
# *******
# *******
# (квадрат из n строк, в каждой строке n звездочек, гарантируется, что  - целое число)
#
#
n = int(input())
for _ in range(n):
    s = ' * ' * n # добавлены пробелы между звездочек, чтобы получился правильный квадрат
    print(s)
